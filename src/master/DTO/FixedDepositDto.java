package master.DTO;

public class FixedDepositDto {
	double fdamt;
	double year;
	String accno;
	String dopen;
	public double getFdamt() {
		return fdamt;
	}
	public void setFdamt(double fdamt) {
		this.fdamt = fdamt;
	}
	public double getYear() {
		return year;
	}
	public void setYear(double year) {
		this.year = year;
	}
	public String getAccno() {
		return accno;
	}
	public void setAccno(String accno) {
		this.accno = accno;
	}
	public String getDopen() {
		return dopen;
	}
	public void setDopen(String dopen) {
		this.dopen = dopen;
	}
	

}
